# core/signal_engine.py
"""
Signal evaluation and Telegram message formatting.

Phase 2 score formula (weights sum to 1.00):
    SIGNAL_SCORE =
        security_score          × 0.20
      + wallet_score            × 0.15
      + rug_score               × 0.15
      + holder_velocity_score   × 0.15
      + smart_money_score       × 0.15
      + tx_pattern_score        × 0.10
      + social_score            × 0.05
      + cross_dex_score         × 0.05

(liquidity_growth_score acts as a gate / tie-breaker rather than a
direct weight component, to avoid over-weighting candle data for
tokens that are <2 minutes old when analysed.)

Gate evaluation order
---------------------
① Social confidence boost (+10 pts when social_score > 60) — BEFORE gates.
② Security score gate           (< 70 → fail)
③ Confidence gate               (< 65 → fail, uses boosted value)
④ Liquidity gate                (< $10k → fail)
⑤ Volume spike gate             (< 2× → fail)
⑥ Wallet CRITICAL gate          → fail
⑦ Rug RUG verdict gate          → fail
⑧ Holder-velocity rug warning   → fail
⑨ Cross-DEX manipulation gate   → fail (gap ≥ 10%)
⑩ Liquidity spike gate          (ARTIFICIAL_SPIKE pattern → warn / reduce score)
⑪ TX pattern gate               (is_artificial_pump → warn / reduce score)
⑫ Composite score gate          (< 50/100 → fail)
"""

from typing import Any, Optional
from loguru import logger
from security.scanner import SecurityResult
from core.token_analyzer import AnalysisResult
from core.solana_scanner import TokenInfo
from config.settings import settings as _settings, runtime_state

# ---------------------------------------------------------------------------
# Phase 2 scoring weights (must sum to 1.00)
# These are updated at startup by load_weights_from_optimal_params()
# ---------------------------------------------------------------------------
_W_SECURITY:         float = 0.20
_W_WALLET:           float = 0.15
_W_RUG:              float = 0.15
_W_HOLDER_VELOCITY:  float = 0.15
_W_SMART_MONEY:      float = 0.15
_W_TX_PATTERN:       float = 0.10
_W_SOCIAL:           float = 0.05
_W_CROSS_DEX:        float = 0.05

# Minimum composite score to emit a signal (0–100)
_MIN_COMPOSITE_SCORE: float = 50.0

# ---------------------------------------------------------------------------
# Public weight accessors (used by backtest_engine to stay in sync)
# ---------------------------------------------------------------------------

def get_signal_weights() -> dict[str, float]:
    return {
        "security":        _W_SECURITY,
        "wallet":          _W_WALLET,
        "rug":             _W_RUG,
        "holder_velocity": _W_HOLDER_VELOCITY,
        "smart_money":     _W_SMART_MONEY,
        "tx_pattern":      _W_TX_PATTERN,
        "social":          _W_SOCIAL,
        "cross_dex":       _W_CROSS_DEX,
    }

def get_min_composite_score() -> float:
    return _MIN_COMPOSITE_SCORE

# ---------------------------------------------------------------------------
# Backtest / ML integration
# ---------------------------------------------------------------------------

def load_weights_from_optimal_params() -> bool:
    """
    Load signal weights and threshold from models/optimal_parameters.json
    generated by the backtest engine.  Idempotent — safe to call on startup.
    Returns True if weights were updated.
    """
    import json
    from pathlib import Path
    global _W_SECURITY, _W_WALLET, _W_RUG, _W_HOLDER_VELOCITY
    global _W_SMART_MONEY, _W_TX_PATTERN, _W_SOCIAL, _W_CROSS_DEX
    global _MIN_COMPOSITE_SCORE

    path = Path(__file__).parent.parent / "models" / "optimal_parameters.json"
    if not path.exists():
        return False
    try:
        data   = json.loads(path.read_text())
        weights = data.get("signal_weights", {})
        if weights:
            _W_SECURITY        = float(weights.get("security",        _W_SECURITY))
            _W_WALLET          = float(weights.get("wallet",          _W_WALLET))
            _W_RUG             = float(weights.get("rug",             _W_RUG))
            _W_HOLDER_VELOCITY = float(weights.get("holder_velocity", _W_HOLDER_VELOCITY))
            _W_SMART_MONEY     = float(weights.get("smart_money",     _W_SMART_MONEY))
            _W_TX_PATTERN      = float(weights.get("tx_pattern",      _W_TX_PATTERN))
            _W_SOCIAL          = float(weights.get("social",          _W_SOCIAL))
            _W_CROSS_DEX       = float(weights.get("cross_dex",       _W_CROSS_DEX))
        min_score = data.get("min_signal_score")
        if min_score is not None:
            _MIN_COMPOSITE_SCORE = float(min_score) * 100.0 \
                if float(min_score) <= 1.0 else float(min_score)
        logger.info(
            "signal_engine: loaded weights from optimal_parameters.json — "
            f"threshold={_MIN_COMPOSITE_SCORE:.0f}/100"
        )
        return True
    except Exception as exc:
        logger.warning(f"signal_engine: could not load optimal params: {exc}")
        return False


# Attempt to load optimised weights at import time (no-op if file absent)
load_weights_from_optimal_params()


