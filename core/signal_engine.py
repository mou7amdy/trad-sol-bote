def format_signal_message(
    token_info:       TokenInfo,
    security_res:     SecurityResult,
    analysis_res:     AnalysisResult,
    wallet_analysis:  Optional[Any] = None,
    rug_analysis:     Optional[Any] = None,
    social_signals:   Optional[Any] = None,
    holder_velocity:  Optional[Any] = None,
    smart_money:      Optional[Any] = None,
    tx_pattern:       Optional[Any] = None,
    liquidity_growth: Optional[Any] = None,
    cross_dex:        Optional[Any] = None,
    composite_score:  float = 0.0,
    ml_pred:          Optional[Any] = None,
) -> str:
    """Build the full Telegram-formatted signal message."""
    mint        = token_info.address
    dex_source  = token_info.dex_source if hasattr(token_info, "dex_source") else "Raydium_AMM"
    latency_ms  = token_info.detection_latency_ms if hasattr(token_info, "detection_latency_ms") else 0.0
    dex_link    = f"https://dexscreener.com/solana/{mint}"

    # ── Header ────────────────────────────────────────────────────────
    msg = (
        f"🚀 **NEW SOLANA SIGNAL DETECTED** 🚀\n\n"
        f"💎 **Token:** {token_info.name} ({token_info.symbol})\n"
        f"📍 **Mint:** `{mint}`\n"
        f"🏦 **DEX:** `{dex_source}`"
        + (f" ⚡ `{latency_ms:.0f}ms`" if latency_ms > 0 else "") + "\n"
        f"💵 **Price:** ${token_info.price:,.6f}\n"
        f"💦 **Liquidity:** ${token_info.liquidity_usd:,.2f}\n"
        f"📊 **Market Cap:** ${token_info.market_cap:,.2f}\n"
        f"🎯 **Signal Score:** `{composite_score:.1f}/100`\n"
    )

    # ── ML Prediction data ────────────────────────────────────────────
    if ml_pred and hasattr(ml_pred, 'ml_score'):
        msg += (
            f"🤖 **ML Score:** `{ml_pred.ml_score:.0f}/100`\n"
            f"⏱ **Best Entry:** `T+{ml_pred.entry_minutes}min`\n"
            f"📈 **Pump Prob:** `{ml_pred.pump_probability:.0%}` | "
            f"🛑 **Rug Prob:** `{ml_pred.rug_probability:.0%}`\n"
        )
    msg += "\n"

    # ── Security ──────────────────────────────────────────────────────
    msg += (
        f"🛡️ **Security Scan:**\n"
        f"  - Score: `{security_res.score:.1f}/100`\n"
        f"  - Status: {'✅ Passed' if security_res.passed else '❌ Failed'}\n"
        f"  - Scan Status: `{security_res.scan_status}`\n"
        f"  - Fail Reason: {security_res.fail_reason or 'None'}\n\n"
    )

    # ── Technical Analysis ────────────────────────────────────────────
    msg += (
        f"📈 **Technical Analysis:**\n"
        f"  - Confidence: `{analysis_res.confidence_score:.1f}/100`\n"
        f"  - RSI (1m): `{analysis_res.rsi:.1f}`\n"
        f"  - Volume Spike: `{analysis_res.volume_spike_ratio:.2f}×`\n"
        f"  - Price Momentum: `{analysis_res.momentum_score:.1f}/100`\n"
    )

    # ── Wallet Analysis ───────────────────────────────────────────────
    if wallet_analysis:
        risk_icon  = _risk_emoji(wallet_analysis.risk_level)
        new_tag    = "  🆕 NEW WALLET" if wallet_analysis.is_new_wallet else ""
        sniper_tag = (
            f" ⚠️ {wallet_analysis.sniper_percentage:.0f}% sniped"
            if wallet_analysis.sniper_percentage > 10.0 else ""
        )
        msg += (
            f"\n👛 **Wallet Analysis:**\n"
            f"  - Creator Age: `{wallet_analysis.wallet_age_days}d`{new_tag}\n"
            f"  - Snipers: `{wallet_analysis.sniper_count}`{sniper_tag}\n"
            f"  - Wallet Score: `{wallet_analysis.wallet_score:.0f}/100`\n"
            f"  - Risk Level: {risk_icon} `{wallet_analysis.risk_level}`\n"
        )

    # ── Rug Detection ─────────────────────────────────────────────────
    if rug_analysis:
        rug_icon  = _rug_emoji(rug_analysis.recommendation)
        flags_str = (
            "\n".join(f"    ⚑ {f}" for f in rug_analysis.risk_flags)
            if rug_analysis.risk_flags else "    None detected"
        )
        msg += (
            f"\n🔍 **Rug Detection:**\n"
            f"  - Rug Probability: `{rug_analysis.rug_probability:.0%}`\n"
            f"  - Pattern Score: `{rug_analysis.pattern_score:.0f}/100`\n"
            f"  - Verdict: {rug_icon} `{rug_analysis.recommendation}`\n"
            f"  - Flags:\n{flags_str}\n"
        )

    # ── Holder Velocity (Phase 1) ─────────────────────────────────────
    if holder_velocity:
        vel_icon  = _velocity_emoji(holder_velocity.velocity_label)
        rug_warn  = "  🩸 RUG WARNING" if holder_velocity.is_rug_warning else ""
        msg += (
            f"\n👥 **Holder Velocity:**\n"
            f"  - Holders: `{holder_velocity.current_holders:,}`{rug_warn}\n"
            f"  - Growth: `{holder_velocity.holders_per_minute:.1f}/min` "
            f"{vel_icon} `{holder_velocity.velocity_label}`\n"
            f"  - Velocity Score: `{holder_velocity.holder_velocity_score:.0f}/100`\n"
        )

    # ── Smart Money (Phase 1) ─────────────────────────────────────────
    if smart_money and smart_money.analyzed_wallets > 0:
        sm_icon = "🧠" if smart_money.smart_money_count > 0 else "👤"
        msg += (
            f"\n{sm_icon} **Smart Money:**\n"
            f"  - First Buyers: `{len(smart_money.first_buyers)}`\n"
            f"  - Smart Money: `{smart_money.smart_money_count}` "
            f"({smart_money.smart_money_pct:.0f}%)\n"
            f"  - SM Score: `{smart_money.smart_money_score:.0f}/100`\n"
        )

    # ── TX Pattern (Phase 2) ──────────────────────────────────────────
    if tx_pattern and tx_pattern.total_txs > 0:
        pump_tag = "  🚨 PUMP" if tx_pattern.is_artificial_pump else ""
        wash_tag = (
            f"  ⚠️ {tx_pattern.wash_trade_count} wash trades"
            if tx_pattern.wash_trade_count > 0 else ""
        )
        msg += (
            f"\n📊 **TX Pattern:**\n"
            f"  - Txs: `{tx_pattern.total_txs}` "
            f"({tx_pattern.buy_count} buy / {tx_pattern.sell_count} sell){pump_tag}\n"
            f"  - Buy Ratio: `{tx_pattern.buy_ratio:.0%}`{wash_tag}\n"
            f"  - Pattern Score: `{tx_pattern.tx_pattern_score:.0f}/100`\n"
        )

    # ── Liquidity Growth (Phase 2) ────────────────────────────────────
    if liquidity_growth and liquidity_growth.candle_count > 0:
        grow_icon = _growth_emoji(liquidity_growth.growth_pattern)
        msg += (
            f"\n💧 **Liquidity Growth:**\n"
            f"  - Pattern: {grow_icon} `{liquidity_growth.growth_pattern}`\n"
            f"  - Candles: `{liquidity_growth.candle_count}` × 1m "
            f"| Vol CV: `{liquidity_growth.volume_cv:.2f}`\n"
            f"  - Price Change: `{liquidity_growth.growth_rate_pct:+.2f}%`\n"
            f"  - Growth Score: `{liquidity_growth.liquidity_growth_score:.0f}/100`\n"
        )

    # ── Cross-DEX (Phase 2) ───────────────────────────────────────────
    if cross_dex and cross_dex.source_count >= 2:
        gap_icon = _gap_emoji(cross_dex.gap_label)
        prices_str = " / ".join(
            f"{src}: ${p:.8f}" for src, p in cross_dex.prices.items()
        )
        msg += (
            f"\n🔀 **Cross-DEX:**\n"
            f"  - Sources: `{cross_dex.source_count}` ({prices_str})\n"
            f"  - Price Gap: `{cross_dex.price_gap_pct:.2f}%` "
            f"{gap_icon} `{cross_dex.gap_label}`\n"
            f"  - Cross-DEX Score: `{cross_dex.cross_dex_score:.0f}/100`\n"
        )

    # ── Social Signals ────────────────────────────────────────────────
    if social_signals:
        viral_tag = " 🔥 VIRAL"       if social_signals.has_viral_tweet     else ""
        inf_tag   = " 🌟 Influencer"  if social_signals.influencer_mentioned else ""
        msg += (
            f"\n📣 **Social Signals:**\n"
            f"  - Mentions (1h): `{social_signals.mention_count_1h}`{viral_tag}{inf_tag}\n"
            f"  - Velocity: `{social_signals.mention_velocity:.2f}/min`\n"
            f"  - Sentiment: `{social_signals.sentiment_score:.0f}/100`\n"
            f"  - Social Score: `{social_signals.social_score:.0f}/100`\n"
        )

    msg += f"\n🔗 [DexScreener]({dex_link})"
    return msg
