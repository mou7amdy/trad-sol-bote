# Gate evaluation
# ---------------------------------------------------------------------------

def evaluate_signal(
    token_info:       TokenInfo,
    security_res:     SecurityResult,
    analysis_res:     AnalysisResult,
    wallet_analysis:  Optional[Any] = None,   # WalletAnalysis
    rug_analysis:     Optional[Any] = None,   # RugAnalysis
    social_signals:   Optional[Any] = None,   # SocialSignals
    holder_velocity:  Optional[Any] = None,   # HolderVelocityResult  (Phase 1)
    smart_money:      Optional[Any] = None,   # FirstBuyerAnalysis    (Phase 1)
    tx_pattern:       Optional[Any] = None,   # TxPatternResult       (Phase 2)
    liquidity_growth: Optional[Any] = None,   # LiquidityGrowthResult (Phase 2)
    cross_dex:        Optional[Any] = None,   # CrossDexResult        (Phase 2)
) -> SignalDecision:
    """
    Evaluate whether a token deserves a signal alert.

    Always returns a SignalDecision — never raises.
    """
    fail_reasons: list[str] = []
    boosted_confidence = analysis_res.confidence_score

    # ── ① Social confidence boost ─────────────────────────────────────
    if social_signals and social_signals.social_score > 60.0:
        boosted_confidence = min(100.0, analysis_res.confidence_score + 10.0)
        logger.info(
            f"Social boost for {token_info.address}: "
            f"confidence {analysis_res.confidence_score:.1f} → {boosted_confidence:.1f}"
        )

    # ── ②–⑤ Core gates ────────────────────────────────────────────────
    if security_res.score < 70.0:
        fail_reasons.append(
            f"Security score too low ({security_res.score:.1f}/100)"
        )
    if boosted_confidence < 65.0:
        fail_reasons.append(
            f"Confidence too low ({analysis_res.confidence_score:.1f}/100)"
        )
    if token_info.liquidity_usd < 10_000.0:
        fail_reasons.append(
            f"Liquidity too low (${token_info.liquidity_usd:,.2f})"
        )
    if analysis_res.volume_spike_ratio < 2.0:
        fail_reasons.append(
            f"Volume spike too weak ({analysis_res.volume_spike_ratio:.2f}×)"
        )

    # ── ⑥ Wallet CRITICAL gate ────────────────────────────────────────
    if wallet_analysis and wallet_analysis.risk_level == "CRITICAL":
        fail_reasons.append(
            f"Creator wallet CRITICAL risk "
            f"(score={wallet_analysis.wallet_score:.0f}/100, "
            f"age={wallet_analysis.wallet_age_days}d)"
        )

    # ── ⑦ Rug verdict gate ────────────────────────────────────────────
    if rug_analysis and rug_analysis.recommendation == "RUG":
        fail_reasons.append(
            f"Rug pull pattern detected ({rug_analysis.rug_probability:.0%})"
        )

    # ── ⑧ Holder-velocity rug warning ────────────────────────────────
    if holder_velocity and holder_velocity.is_rug_warning:
        fail_reasons.append(
            f"Holder-drop rug warning "
            f"({holder_velocity.current_holders} holders, "
            f"peak was {holder_velocity.peak_holders})"
        )

    # ── ⑨ Cross-DEX manipulation gate ────────────────────────────────
    if cross_dex and cross_dex.is_manipulated:
        fail_reasons.append(
            f"Cross-DEX price manipulation detected "
            f"(gap={cross_dex.price_gap_pct:.1f}% across {cross_dex.source_count} sources)"
        )

    # ── ⑩–⑪ Soft gates (reduce score, add warning, don't hard-fail) ──
    # These are already baked into the module scores; logged here for audit.
    if liquidity_growth and liquidity_growth.growth_pattern == "ARTIFICIAL_SPIKE":
        logger.warning(
            f"Artificial liquidity spike pattern for {token_info.address} "
            f"(first-candle vol = {liquidity_growth.first_candle_vol_pct:.0%})"
        )
    if tx_pattern and tx_pattern.is_artificial_pump:
        logger.warning(
            f"Artificial pump tx pattern for {token_info.address} "
            f"(buy_ratio={tx_pattern.buy_ratio:.0%}, wash={tx_pattern.wash_trade_count})"
        )

    # ── ⑫ ML prediction (Part 4) ─────────────────────────────────────
    ml_pred: Optional[Any] = None
    try:
        from backtesting.ml_optimizer import MLOptimizer
        _ml_opt = MLOptimizer()
        if _ml_opt.load_models():
            ml_pred = _ml_opt.predict(token_info, analysis_res, security_res, wallet_analysis)
    except Exception:
        pass

    ml_gates_active = ml_pred is not None and hasattr(ml_pred, 'should_signal')

    if ml_gates_active:
        # Replace confidence gate with ML pump probability
        if ml_pred.pump_probability < 0.55:
            fail_reasons.append(
                f"ML pump probability too low ({ml_pred.pump_probability:.0%})"
            )
        # Replace rug gate with ML rug probability
        if ml_pred.rug_probability > 0.35:
            fail_reasons.append(
                f"ML rug probability too high ({ml_pred.rug_probability:.0%})"
            )
    else:
        # ── ⑫–⑬ ML not available — use rule-based gates ──────────────
        # Rug gate already handled above at ⑦
        pass

    # ── ⑭ Composite SIGNAL_SCORE ─────────────────────────────────────
    NEUTRAL = 50.0
    sec_score  = security_res.score
    wal_score  = wallet_analysis.wallet_score           if wallet_analysis  else NEUTRAL
    rug_score  = rug_analysis.pattern_score             if rug_analysis     else NEUTRAL
    hv_score   = holder_velocity.holder_velocity_score  if holder_velocity  else NEUTRAL
    sm_score   = smart_money.smart_money_score          if smart_money      else NEUTRAL
    txp_score  = tx_pattern.tx_pattern_score            if tx_pattern       else NEUTRAL
    soc_score  = social_signals.social_score            if social_signals   else NEUTRAL
    cdx_score  = cross_dex.cross_dex_score              if cross_dex        else NEUTRAL

    composite = (
        sec_score * _W_SECURITY
        + wal_score  * _W_WALLET
        + rug_score  * _W_RUG
        + hv_score   * _W_HOLDER_VELOCITY
        + sm_score   * _W_SMART_MONEY
        + txp_score  * _W_TX_PATTERN
        + soc_score  * _W_SOCIAL
        + cdx_score  * _W_CROSS_DEX
    )

    if ml_gates_active:
        ml_score_weight = 0.10
        weighted_ml = ml_pred.ml_score * ml_score_weight
        remaining = 1.0 - ml_score_weight
        composite = composite * remaining + weighted_ml

    composite = round(composite, 2)

    logger.info(
        f"SIGNAL_SCORE for {token_info.address}: {composite:.1f}/100 "
        f"[sec={sec_score:.0f} wal={wal_score:.0f} rug={rug_score:.0f} "
        f"hv={hv_score:.0f} sm={sm_score:.0f} txp={txp_score:.0f} "
        f"soc={soc_score:.0f} cdx={cdx_score:.0f}]"
        + (f" ml={ml_pred.ml_score:.0f}" if ml_gates_active else "")
    )

    if composite < _MIN_COMPOSITE_SCORE and not fail_reasons:
        fail_reasons.append(
            f"Composite signal score too low ({composite:.1f}/100 < {_MIN_COMPOSITE_SCORE})"
        )

    send   = len(fail_reasons) == 0
    reason = "All checks passed!" if send else "; ".join(fail_reasons)

    # --- Auto-buy recommendation ---
    min_score_threshold = runtime_state.min_signal_score_for_buy
    buy_recommended = send and (composite >= min_score_threshold)

    return SignalDecision(
        send=send,
        reason=reason,
        composite_score=composite,
        ml_pred=ml_pred,
        buy_recommended=buy_recommended,
    )


# ---------------------------------------------------------------------------
# Private emoji helpers
# ---------------------------------------------------------------------------

def _risk_emoji(level: str) -> str:
    return {"LOW": "🟢", "MEDIUM": "🟡", "HIGH": "🟠", "CRITICAL": "🔴"}.get(level, "⚪")

def _rug_emoji(rec: str) -> str:
    return {"SAFE": "✅", "CAUTION": "⚠️", "AVOID": "🚫", "RUG": "💀"}.get(rec, "❓")

def _velocity_emoji(label: str) -> str:
    return {
        "STRONG":   "🚀",
        "MODERATE": "📈",
        "SLOW":     "🐌",
        "STAGNANT": "😐",
        "DROPPING": "🩸",
    }.get(label, "❓")

def _growth_emoji(pattern: str) -> str:
    return {
        "ORGANIC":           "🌱",
        "CONSISTENT":        "📊",
        "FLATLINE":          "➡️",
        "DECLINING":         "📉",
        "ARTIFICIAL_SPIKE":  "🚨",
        "INSUFFICIENT_DATA": "❓",
    }.get(pattern, "❓")

def _gap_emoji(label: str) -> str:
    return {
        "HEALTHY":      "✅",
        "ACCEPTABLE":   "🟡",
        "SUSPICIOUS":   "🟠",
        "MANIPULATED":  "🔴",
        "UNKNOWN":      "⚪",
    }.get(label, "⚪")


